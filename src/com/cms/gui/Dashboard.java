    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Student input = new Student();
        input.setAdmissionNumber(ad_no.getText());
        input.setName(name.getText());
        input.setGrade(grade.getText());
        input.setFess(fees.getText());

        if ("Update".equals(jButton1.getText())) {
            service.updateStudent(input);
        } else {
            service.addStudent(input);
        }

        clearInputFields();
        jButton1.setText("Add");
        clearAndLoadTableData();
    }//GEN-LAST:event_jButton1ActionPerformed

