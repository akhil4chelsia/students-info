    private void clearAndLoadTableData() {
        clearTableData();
        loadTableData();
    }

    private void loadTableData() {

        DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();

        for (Student each : service.getStudents()) {
            dm.addRow(new Object[]{each.getAdmissionNumber(), each.getName(), each.getGrade(), each.getFess()});
        }
    }

    private void clearTableData() {
        int i = 0;
        DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();
        int rowCount = dm.getRowCount();
//Remove rows one by one from the end of the table
        for (int j = rowCount - 1; j >= 0; j--) {
            dm.removeRow(j);
        }
    }

    private void clearInputFields() {
        ad_no.setText("");
        name.setText("");
        grade.setText("");
        fees.setText("");
    }

